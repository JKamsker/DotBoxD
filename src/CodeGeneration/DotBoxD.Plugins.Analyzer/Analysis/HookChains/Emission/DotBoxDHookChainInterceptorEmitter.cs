using System.Collections.Immutable;
using System.Text;
using DotBoxD.Plugins.Analyzer.Analysis.Lowering;
using Microsoft.CodeAnalysis;

namespace DotBoxD.Plugins.Analyzer.Analysis.HookChains;

/// <summary>
/// Emits the C# interceptors that replace each lowered chain's <c>Run(lambda)</c> call site
/// with <c>Run(lambda, IRKernel.FromPackage(&lt;ChainPackage&gt;.Create()))</c>, plus the
/// <c>InterceptsLocationAttribute</c> definition. Consuming projects opt in by adding the interceptor
/// namespace to <c>&lt;InterceptorsNamespaces&gt;</c> (<c>DotBoxD.Plugins.Generated</c>).
/// </summary>
internal static class DotBoxDHookChainInterceptorEmitter
{
    public const string InterceptorNamespace = DotBoxDGenerationNames.TypeNames.GeneratedInterceptorsNamespace;

    public static void Emit(SourceProductionContext context, ImmutableArray<HookChainInterception> interceptions)
    {
        if (interceptions.IsDefaultOrEmpty)
        {
            return;
        }

        context.AddSource("DotBoxDHookChainInterceptors.g.cs", BuildInterceptors(interceptions));
    }

    private static string BuildInterceptors(ImmutableArray<HookChainInterception> interceptions)
    {
        var builder = new StringBuilder();
        builder.AppendLine("// <auto-generated/>");
        builder.AppendLine("#nullable enable");
        InterceptsLocationAttributeEmitter.Append(builder);
        builder.Append("namespace ").AppendLine(DotBoxDGenerationNames.TypeNames.GeneratedInterceptorsNamespace);
        builder.AppendLine("{");
        builder.AppendLine("    internal static class HookChainInterceptors");
        builder.AppendLine("    {");

        for (var i = 0; i < interceptions.Length; i++)
        {
            var interception = interceptions[i];
            builder.Append("        ").AppendLine(interception.AttributeSyntax);
            AppendGeneratedAttributes(builder, interception.GeneratedAttributeSource);
            builder.Append("        public static ").Append(interception.ReturnTypeFullName).Append(" Intercept_")
                .Append(i.ToString(System.Globalization.CultureInfo.InvariantCulture));
            // An un-nameable (anonymous) terminal projection makes the interceptor generic; Roslyn infers the type
            // parameters from the intercepted call site, so the source never names the type. The arity must match
            // the interceptable method's generic context, so the full receiver type-parameter list is emitted.
            if (interception.InterceptorTypeParameters is { } interceptorTypeParameters)
            {
                builder.Append('<').Append(interceptorTypeParameters).Append('>');
            }

            builder.AppendLine("(");
            builder.Append("            this ").Append(interception.ReceiverTypeFullName).AppendLine(" pipeline,");
            if (IsResultChain(interception.InstallKind))
            {
                builder.Append("            ").Append(interception.HandlerTypeFullName).AppendLine(" handler,");
                builder.AppendLine("            global::DotBoxD.Plugins.IRKernel? irHandler = null,");
                builder.AppendLine("            int priority = 0)");
                builder.Append("            => pipeline.")
                    .Append(interception.InstallKind == HookChainInterceptorInstallKind.ResultChain
                        ? "Register"
                        : "RegisterLocal")
                    .Append('<').Append(interception.ResultTypeFullName).Append(">(")
                    .Append("handler, ")
                    .Append(IrKernelExpression(interception))
                    .Append(", priority")
                    .AppendLine(");");
                continue;
            }

            builder.Append("            ").Append(interception.HandlerTypeFullName).AppendLine(" handler,");
            builder.AppendLine("            global::DotBoxD.Plugins.IRKernel? irHandler = null)");
            builder.Append("            => pipeline.")
                .Append(TerminalMethod(interception))
                .Append('(')
                .Append("handler, ")
                .Append(IrKernelExpression(interception))
                .AppendLine(");");
        }

        builder.AppendLine("    }");
        builder.AppendLine("}");
        return builder.ToString();
    }

    private static void AppendGeneratedAttributes(StringBuilder builder, string attributeSource)
    {
        if (attributeSource.Length == 0)
        {
            return;
        }

        foreach (var line in attributeSource.Split('\n'))
        {
            if (line.Length > 0)
            {
                builder.Append("        ").AppendLine(line);
            }
        }
    }

    private static string TerminalMethod(HookChainInterception interception)
        => interception.InstallKind == HookChainInterceptorInstallKind.LocalCallback ? "RunLocal" : "Run";

    private static string IrKernelExpression(HookChainInterception interception)
    {
        if (!interception.HasLocalDecoder)
        {
            return "global::DotBoxD.Plugins.IRKernel.FromPackage(" +
                interception.PackageFullName +
                ".Create())";
        }

        var decoder = interception.LocalDecoderTypeArgument is { Length: > 0 } typeArgument
            ? interception.PackageFullName + ".ReadProjectedPayload<" + typeArgument + ">"
            : interception.PackageFullName + ".ReadProjectedPayload";
        return "global::DotBoxD.Plugins.IRKernel.FromPackage(" +
            interception.PackageFullName +
            ".Create(), " +
            decoder +
            ")";
    }

    private static bool IsResultChain(HookChainInterceptorInstallKind kind)
        => kind is HookChainInterceptorInstallKind.ResultChain or HookChainInterceptorInstallKind.LocalResultChain;
}
