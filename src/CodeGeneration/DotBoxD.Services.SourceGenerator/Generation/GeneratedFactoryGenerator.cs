using System.Collections.Generic;
using System.Text;
using System.Threading;
using DotBoxD.Services.SourceGenerator.Infrastructure;
using DotBoxD.Services.SourceGenerator.Models;

namespace DotBoxD.Services.SourceGenerator.Generation;

internal static class GeneratedFactoryGenerator
{
    public static string Generate(
        EquatableArray<ServiceModel> services,
        bool emitClsNonCompliantAttribute,
        CancellationToken ct = default)
    {
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        GeneratedWarningPragmaEmitter.AppendDisable(sb, ExperimentalDiagnosticIds(services));
        sb.AppendLine();
        sb.Append("namespace ").AppendLine(ServicesGeneratorTypeNames.GeneratedNamespace);
        sb.AppendLine("{");
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Factory methods for DotBoxD-generated proxies and dispatchers.");
        sb.AppendLine("    /// </summary>");
        if (emitClsNonCompliantAttribute)
        {
            sb.AppendLine("    [global::System.CLSCompliant(false)]");
        }

        sb.AppendLine("    public static class DotBoxDGenerated");
        sb.AppendLine("    {");
        GeneratedFactoryMetadataEmitter.AppendMethodArrays(sb, services, ct);
        sb.AppendLine();
        sb.AppendLine($"        private static readonly {ServicesGeneratorTypeNames.Generic(ServicesGeneratorTypeNames.GlobalReadOnlyList, ServicesGeneratorTypeNames.GlobalGeneratedService)} s_services =");
        sb.AppendLine($"            {ServicesGeneratorTypeNames.GlobalArray}.AsReadOnly(new {ServicesGeneratorTypeNames.ArrayOf(ServicesGeneratorTypeNames.GlobalGeneratedService)}");
        sb.AppendLine("        {");

        for (var i = 0; i < services.Array.Length; i++)
        {
            ct.ThrowIfCancellationRequested();
            var service = services.Array[i];
            var serviceName = NamingHelpers.StripInterfacePrefix(service.InterfaceName);
            var fullInterfaceName = IdentifierHelpers.QualifyTypeName(service.Namespace, service.InterfaceName);
            var fullProxyName = IdentifierHelpers.QualifyTypeName(service.Namespace, serviceName + "Proxy");
            var fullDispatcherName = IdentifierHelpers.QualifyTypeName(service.Namespace, serviceName + "Dispatcher");

            AppendObsoletePragmaStart(sb, service);
            sb.AppendLine($"            new {ServicesGeneratorTypeNames.GlobalGeneratedService}(");
            sb.AppendLine($"                typeof({fullInterfaceName}),");
            sb.AppendLine($"                typeof({fullProxyName}),");
            sb.AppendLine($"                typeof({fullDispatcherName}),");
            // service.ServiceName is already escaped at model-build time (ServiceModelFactory),
            // matching every other emission site (proxy/dispatcher). Inserting it directly avoids
            // double-escaping names that contain backslashes, quotes, or control characters.
            sb.AppendLine($"                \"{service.ServiceName}\",");
            sb.AppendLine($"                {GeneratedFactoryMetadataEmitter.MethodArrayName(i)}),");
            AppendObsoletePragmaEnd(sb, service);
        }

        sb.AppendLine("        });");
        sb.AppendLine();
        sb.AppendLine("        static DotBoxDGenerated()");
        sb.AppendLine("        {");
        sb.AppendLine($"            {ServicesGeneratorTypeNames.GlobalGeneratedServiceRegistry}.{ServicesGeneratorMemberNames.GeneratedServiceRegistry.RegisterServices}(");
        sb.AppendLine($"                typeof({ServicesGeneratorTypeNames.GlobalDotBoxDGenerated}).Assembly,");
        sb.AppendLine("                s_services);");

        for (var i = 0; i < services.Array.Length; i++)
        {
            ct.ThrowIfCancellationRequested();
            var service = services.Array[i];
            var serviceName = NamingHelpers.StripInterfacePrefix(service.InterfaceName);
            var fullInterfaceName = IdentifierHelpers.QualifyTypeName(service.Namespace, service.InterfaceName);
            var fullProxyName = IdentifierHelpers.QualifyTypeName(service.Namespace, serviceName + "Proxy");
            var fullDispatcherName = IdentifierHelpers.QualifyTypeName(service.Namespace, serviceName + "Dispatcher");

            AppendObsoletePragmaStart(sb, service);
            sb.AppendLine($"            {ServicesGeneratorTypeNames.GlobalGeneratedServiceRegistry}.{ServicesGeneratorMemberNames.GeneratedServiceRegistry.Register}<{fullInterfaceName}>(");
            sb.AppendLine($"                static client => new {fullProxyName}(client),");
            sb.AppendLine($"                static implementation => new {fullDispatcherName}(({fullInterfaceName})implementation),");
            sb.AppendLine($"                s_services[{i.ToString(System.Globalization.CultureInfo.InvariantCulture)}]);");
            AppendObsoletePragmaEnd(sb, service);
        }

        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// Gets the services generated into this assembly without scanning for generated types.");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine($"        public static {ServicesGeneratorTypeNames.Generic(ServicesGeneratorTypeNames.GlobalReadOnlyList, ServicesGeneratorTypeNames.GlobalGeneratedService)} Services");
        sb.AppendLine("            => s_services;");
        sb.AppendLine();
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// Adds generated service proxy registrations to <paramref name=\"sink\" /> without scanning generated types.");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine($"        public static void RegisterServices({ServicesGeneratorTypeNames.GlobalServiceRegistrationSink} sink)");
        sb.AppendLine("        {");
        sb.AppendLine("            if (sink is null)");
        sb.AppendLine("            {");
        sb.AppendLine($"                throw new {ServicesGeneratorTypeNames.GlobalArgumentNullException}(nameof(sink));");
        sb.AppendLine("            }");
        sb.AppendLine();

        foreach (var service in services.Array)
        {
            ct.ThrowIfCancellationRequested();
            var serviceName = NamingHelpers.StripInterfacePrefix(service.InterfaceName);
            var fullInterfaceName = IdentifierHelpers.QualifyTypeName(service.Namespace, service.InterfaceName);
            var fullProxyName = IdentifierHelpers.QualifyTypeName(service.Namespace, serviceName + "Proxy");

            AppendObsoletePragmaStart(sb, service);
            sb.AppendLine($"            sink.{ServicesGeneratorMemberNames.ServiceRegistrationSink.AddService}<{fullInterfaceName}, {fullProxyName}>();");
            AppendObsoletePragmaEnd(sb, service);
        }

        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// Adds generated service proxy and dispatcher registrations to <paramref name=\"sink\" /> without scanning generated types.");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine($"        public static void RegisterGeneratedServices({ServicesGeneratorTypeNames.GlobalGeneratedServiceRegistrationSink} sink)");
        sb.AppendLine("        {");
        sb.AppendLine("            if (sink is null)");
        sb.AppendLine("            {");
        sb.AppendLine($"                throw new {ServicesGeneratorTypeNames.GlobalArgumentNullException}(nameof(sink));");
        sb.AppendLine("            }");
        sb.AppendLine();

        foreach (var service in services.Array)
        {
            ct.ThrowIfCancellationRequested();
            var serviceName = NamingHelpers.StripInterfacePrefix(service.InterfaceName);
            var fullInterfaceName = IdentifierHelpers.QualifyTypeName(service.Namespace, service.InterfaceName);
            var fullProxyName = IdentifierHelpers.QualifyTypeName(service.Namespace, serviceName + "Proxy");
            var fullDispatcherName = IdentifierHelpers.QualifyTypeName(service.Namespace, serviceName + "Dispatcher");

            AppendObsoletePragmaStart(sb, service);
            sb.AppendLine($"            sink.{ServicesGeneratorMemberNames.ServiceRegistrationSink.AddService}<{fullInterfaceName}, {fullProxyName}, {fullDispatcherName}>();");
            AppendObsoletePragmaEnd(sb, service);
        }

        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// Creates the generated proxy for <typeparamref name=\"TService\" /> over an invoker.");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine($"        public static TService CreateProxy<TService>({ServicesGeneratorTypeNames.GlobalRpcInvoker} invoker)");
        sb.AppendLine("            where TService : class");
        sb.AppendLine($"            => {ServicesGeneratorTypeNames.GlobalGeneratedServiceRegistry}.{ServicesGeneratorMemberNames.GeneratedServiceRegistry.CreateProxy}<TService>(invoker);");
        sb.AppendLine();
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// Creates the generated proxy for <paramref name=\"serviceInterface\" /> over an invoker.");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine($"        public static object CreateProxy({ServicesGeneratorTypeNames.GlobalType} serviceInterface, {ServicesGeneratorTypeNames.GlobalRpcInvoker} invoker)");
        sb.AppendLine($"            => {ServicesGeneratorTypeNames.GlobalGeneratedServiceRegistry}.{ServicesGeneratorMemberNames.GeneratedServiceRegistry.CreateProxy}(serviceInterface, invoker);");
        sb.AppendLine();
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// Creates the generated server dispatcher for <paramref name=\"implementation\" />.");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine($"        public static {ServicesGeneratorTypeNames.GlobalServiceDispatcher} CreateDispatcher<TService>(TService implementation)");
        sb.AppendLine("            where TService : class");
        sb.AppendLine($"            => {ServicesGeneratorTypeNames.GlobalGeneratedServiceRegistry}.{ServicesGeneratorMemberNames.GeneratedServiceRegistry.CreateDispatcher}<TService>(implementation);");
        sb.AppendLine();
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// Creates the generated server dispatcher for <paramref name=\"implementation\" />.");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine($"        public static {ServicesGeneratorTypeNames.GlobalServiceDispatcher} CreateDispatcher({ServicesGeneratorTypeNames.GlobalType} serviceInterface, object implementation)");
        sb.AppendLine($"            => {ServicesGeneratorTypeNames.GlobalGeneratedServiceRegistry}.{ServicesGeneratorMemberNames.GeneratedServiceRegistry.CreateDispatcher}(serviceInterface, implementation);");
        sb.AppendLine("    }");
        sb.AppendLine("}");
        GeneratedWarningPragmaEmitter.AppendRestore(sb, ExperimentalDiagnosticIds(services));

        return sb.ToString();
    }

    private static void AppendObsoletePragmaStart(StringBuilder sb, ServiceModel service)
    {
        if (service.ObsoleteAttribute.Length > 0)
        {
            sb.AppendLine("#pragma warning disable CS0618, CS0619");
        }
    }

    private static void AppendObsoletePragmaEnd(StringBuilder sb, ServiceModel service)
    {
        if (service.ObsoleteAttribute.Length > 0)
        {
            sb.AppendLine("#pragma warning restore CS0618, CS0619");
        }
    }

    private static IEnumerable<string> ExperimentalDiagnosticIds(EquatableArray<ServiceModel> services)
    {
        foreach (var service in services.Array)
        {
            yield return service.ExperimentalDiagnosticId;
        }
    }
}
