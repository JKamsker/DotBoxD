using System.Text;
using System.Threading;
using DotBoxD.Services.SourceGenerator.Infrastructure;
using DotBoxD.Services.SourceGenerator.Models;

namespace DotBoxD.Services.SourceGenerator.Generation;

internal static partial class ProxyGenerator
{
    private static string ProxyBaseList(
        string qualifiedInterface,
        string qualifiedAsyncSibling,
        EquatableArray<AsyncSiblingMethod> siblingMethods)
        => siblingMethods.IsEmpty
            ? qualifiedInterface
            : $"{qualifiedInterface}, {qualifiedAsyncSibling}";

    private static void AppendProxyHeader(
        StringBuilder sb,
        ServiceModel service,
        string proxyName,
        string baseList,
        bool emitClsNonCompliantAttribute)
    {
        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        GeneratedWarningPragmaEmitter.AppendDisable(sb, service.ExperimentalDiagnosticId);
        sb.AppendLine();
        AppendNamespaceStart(sb, service);
        sb.AppendLine("    /// <summary>");
        sb.AppendLine($"    /// Client proxy for {service.InterfaceName}.");
        sb.AppendLine("    /// </summary>");
        ObsoleteAttributeEmitter.AppendIfPresent(sb, service.ObsoleteAttribute, "    ");
        sb.Append(service.TypeAttributePrefix);
        if (emitClsNonCompliantAttribute)
        {
            sb.AppendLine("    [global::System.CLSCompliant(false)]");
        }

        sb.AppendLine($"    public sealed class {proxyName} : {baseList}");
        sb.AppendLine("    {");
        sb.AppendLine($"        private readonly {ServicesGeneratorTypeNames.GlobalRpcInvoker} _invoker;");
        sb.AppendLine("        /// <summary>Non-null when this proxy targets a sub-service instance returned by a parent call.</summary>");
        sb.AppendLine("        private readonly string? _instanceId;");
    }

    private static void AppendNamespaceStart(StringBuilder sb, ServiceModel service)
    {
        if (!string.IsNullOrEmpty(service.Namespace))
        {
            sb.AppendLine($"namespace {IdentifierHelpers.EscapeNamespace(service.Namespace)}");
            sb.AppendLine("{");
        }
    }

    private static void AppendProxyFields(
        StringBuilder sb,
        ServiceModel service,
        CancellationToken ct)
    {
        foreach (var property in service.Properties.Array)
        {
            ct.ThrowIfCancellationRequested();
            if (!property.IsInstanceId)
            {
                sb.AppendLine($"        private readonly {property.Type} {PropertyFieldName(property)};");
            }
        }
    }

    private static void AppendProxyConstructors(
        StringBuilder sb,
        ServiceModel service,
        string proxyName,
        CancellationToken ct)
    {
        sb.AppendLine();
        AppendRootProxyConstructor(sb, service, proxyName, ct);
        sb.AppendLine();
        AppendInstanceProxyConstructor(sb, service, proxyName, ct);
    }

    private static void AppendRootProxyConstructor(
        StringBuilder sb,
        ServiceModel service,
        string proxyName,
        CancellationToken ct)
    {
        sb.AppendLine($"        public {proxyName}({ServicesGeneratorTypeNames.GlobalRpcInvoker} client)");
        sb.AppendLine("        {");
        sb.AppendLine($"            this._invoker = client ?? throw new {ServicesGeneratorTypeNames.GlobalArgumentNullException}(nameof(client));");
        sb.AppendLine("            this._instanceId = null;");
        AppendPropertyInitializers(sb, service, ct);
        sb.AppendLine("        }");
    }

    private static void AppendInstanceProxyConstructor(
        StringBuilder sb,
        ServiceModel service,
        string proxyName,
        CancellationToken ct)
    {
        sb.AppendLine($"        /// <summary>Constructs a proxy bound to a specific server-side instance.</summary>");
        sb.AppendLine($"        public {proxyName}({ServicesGeneratorTypeNames.GlobalRpcInvoker} client, string instanceId)");
        sb.AppendLine("        {");
        sb.AppendLine($"            this._invoker = client ?? throw new {ServicesGeneratorTypeNames.GlobalArgumentNullException}(nameof(client));");
        sb.AppendLine($"            this._instanceId = instanceId ?? throw new {ServicesGeneratorTypeNames.GlobalArgumentNullException}(nameof(instanceId));");
        AppendPropertyInitializers(sb, service, ct);
        sb.AppendLine("        }");
    }

    private static void AppendPropertyInitializers(
        StringBuilder sb,
        ServiceModel service,
        CancellationToken ct)
    {
        foreach (var property in service.Properties.Array)
        {
            ct.ThrowIfCancellationRequested();
            if (!property.IsInstanceId)
            {
                sb.AppendLine($"            this.{PropertyFieldName(property)} = new {property.ProxyType}(client);");
            }
        }
    }

    private static void AppendProxyProperties(
        StringBuilder sb,
        ServiceModel service,
        CancellationToken ct)
    {
        foreach (var property in service.Properties.Array)
        {
            ct.ThrowIfCancellationRequested();
            sb.AppendLine();
            AppendProxyProperty(sb, property);
        }
    }

    private static void AppendProxyProperty(StringBuilder sb, ServicePropertyModel property)
    {
        ProxyGenerationHelpers.AppendAttributes(sb, property.PropertyAttributePrefix);
        ProxyGenerationHelpers.AppendAttributes(sb, property.MemberAttributePrefix);
        if (property.IsInstanceId)
        {
            sb.AppendLine($"        public {property.Type} {property.Name} => this._instanceId ?? string.Empty;");
            return;
        }

        sb.AppendLine($"        public {property.Type} {property.Name} => this.{PropertyFieldName(property)};");
    }

    private static void AppendProxyMethods(
        StringBuilder sb,
        ServiceModel service,
        string proxyName,
        CancellationToken ct)
    {
        foreach (var method in service.Methods.Array)
        {
            ct.ThrowIfCancellationRequested();
            AppendProxyMethodGroup(sb, service, method, proxyName, ct);
        }
    }

    private static void AppendProxyMethodGroup(
        StringBuilder sb,
        ServiceModel service,
        MethodModel method,
        string proxyName,
        CancellationToken ct)
    {
        sb.AppendLine();
        GenerateProxyMethod(sb, service, method, proxyName, ct);
        if (!ProxyGenerationHelpers.MethodNameRequiresExplicitImplementation(method.Name, proxyName))
        {
            return;
        }

        foreach (var implementationType in method.AdditionalExplicitImplementationTypes.Array)
        {
            ct.ThrowIfCancellationRequested();
            sb.AppendLine();
            GenerateProxyMethod(
                sb,
                service,
                method with { ExplicitImplementationType = implementationType },
                proxyName,
                ct);
        }
    }

    private static void AppendAsyncSiblingMethods(
        StringBuilder sb,
        ServiceModel service,
        EquatableArray<AsyncSiblingMethod> siblingMethods,
        string proxyName,
        string qualifiedAsyncSibling,
        CancellationToken ct)
    {
        foreach (var sibling in siblingMethods.Array)
        {
            ct.ThrowIfCancellationRequested();
            if (ShouldEmitAsyncSiblingMethod(sibling, proxyName))
            {
                sb.AppendLine();
                GenerateAsyncSiblingMethod(sb, service, sibling, proxyName, qualifiedAsyncSibling, ct);
            }
        }
    }

    private static bool ShouldEmitAsyncSiblingMethod(AsyncSiblingMethod sibling, string proxyName)
        => sibling.RequiresExtraProxyMethod ||
           ProxyGenerationHelpers.MethodNameRequiresExplicitImplementation(sibling.Name, proxyName);

    private static void AppendProxyFooter(StringBuilder sb, ServiceModel service)
    {
        sb.AppendLine("    }");
        if (!string.IsNullOrEmpty(service.Namespace))
        {
            sb.AppendLine("}");
        }

        GeneratedWarningPragmaEmitter.AppendRestore(sb, service.ExperimentalDiagnosticId);
    }

}
